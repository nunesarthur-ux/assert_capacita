#ifndef ASSERT_TYPES_H_
#define ASSERT_TYPES_H_

#include <stdint.h>

/* Definições de tipos inteiros sem sinal */
typedef uint8_t   u8;
typedef uint16_t  u16;
typedef uint32_t  u32;
typedef uint64_t  u64;

/* Definições de tipos inteiros com sinal */
typedef int8_t    s8;
typedef int16_t   s16;
typedef int32_t   s32;
typedef int64_t   s64;



#endif /* INC_ASSERTTYPES_H_ */
